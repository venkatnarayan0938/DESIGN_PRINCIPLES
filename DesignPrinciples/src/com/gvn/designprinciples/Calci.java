package com.gvn.designprinciples;

public class Calci 
{
	public float Add(float a,float b)
	{
		return a+b;
	}
	public float Subb(float a,float b)
	{
		return a-b;
	}
	public float Mul(float a,float b)
	{
		return a*b;
	}
	public float Div(float a,float b)
	{
		return a/b;
	}
	public float Add(float a,float b,float c)
	{
		return a+b+c;
	}
	public float Subb(float a,float b,float c)
	{
		return a-b-c;
	}
	public float Mul(float a,float b,float c)
	{
		return a*b*c;
	}
	public float Div(float a,float b,float c)
	{
		return (a/b)/c;
	}
}